////////////////////////////////////////////////////////////////////////////////
#ifndef GADGET_LED_HPP
#define GADGET_LED_HPP

////////////////////////////////////////////////////////////////////////////////
#include <gadget++/types.hpp>
#include <gpio++/pin.hpp>

#include <asio/io_service.hpp>

////////////////////////////////////////////////////////////////////////////////
namespace gadget
{

////////////////////////////////////////////////////////////////////////////////
class led
{
public:
    ////////////////////
    led(gpio::pin*);

    led(led&&) noexcept;
    led& operator=(led&&) noexcept;

    ////////////////////
    void turn(gadget::state);
    gadget::state state();

    void on() { turn(gadget::on); }
    void off() { turn(gadget::off); }

    void dim(percent);
    percent level() const;

protected:
    ////////////////////
    gpio::pin* pin_;
};

////////////////////////////////////////////////////////////////////////////////
}

////////////////////////////////////////////////////////////////////////////////
#endif
